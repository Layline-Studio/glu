export type Translate = (
  key: StringKey,
  params?: Record<string, string | number>,
) => string;

export function makeT(locale: ReportLocale): Translate {
  const strings = STRINGS[locale];
  return (key, params) => {
    let value = strings[key] ?? EN[key];
    if (params) {
      for (const [name, replacement] of Object.entries(params)) {
        value = value.replaceAll(`{${name}}`, String(replacement));
      }
    }
    return value;
  };
}

export type DateFormatters = {
  short: (d: Date) => string;
  long: (d: Date) => string;
  monthShort: (d: Date) => string;
};

export function makeDateFmt(
  locale: ReportLocale,
  timeZone: string,
): DateFormatters {
  const short = new Intl.DateTimeFormat(locale, {
    timeZone,
    month: "short",
    day: "numeric",
  });
  const long = new Intl.DateTimeFormat(locale, {
    timeZone,
    year: "numeric",
    month: "long",
    day: "numeric",
  });
  const monthShort = new Intl.DateTimeFormat(locale, {
    timeZone,
    month: "short",
  });
  return {
    short: (d) => short.format(d),
    long: (d) => long.format(d),
    monthShort: (d) => monthShort.format(d),
  };
}

export type NumberFormatters = {
  n0: (v: number) => string;
  n1: (v: number) => string;
  signed1: (v: number) => string;
  pct: (v: number) => string;
};

export function makeNumFmt(locale: ReportLocale): NumberFormatters {
  const n0 = new Intl.NumberFormat(locale, { maximumFractionDigits: 0 });
  const n1 = new Intl.NumberFormat(locale, {
    minimumFractionDigits: 1,
    maximumFractionDigits: 1,
  });
  const signed1 = new Intl.NumberFormat(locale, {
    minimumFractionDigits: 1,
    maximumFractionDigits: 1,
    signDisplay: "always",
  });
  return {
    n0: (v) => n0.format(v),
    n1: (v) => n1.format(v),
    signed1: (v) => signed1.format(v),
    pct: (v) => `${n0.format(v)}%`,
  };
}
