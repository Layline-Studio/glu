export type ReportLocale =
  | "en"
  | "da"
  | "de"
  | "es"
  | "fi"
  | "fr"
  | "it"
  | "nl"
  | "no"
  | "pt"
  | "ru"
  | "sv"
  | "zh";

const SUPPORTED: ReadonlySet<string> = new Set([
  "en",
  "da",
  "de",
  "es",
  "fi",
  "fr",
  "it",
  "nl",
  "no",
  "pt",
  "ru",
  "sv",
  "zh",
]);

export function resolveReportLocale(
  appLocale: string | null | undefined,
): ReportLocale {
  const value = appLocale?.trim();
  if (!value) return "en";
  const normalized = value.replaceAll("_", "-").split("-")[0]?.toLowerCase();
  if (!normalized || !SUPPORTED.has(normalized)) return "en";
  return normalized as ReportLocale;
}

